    {
        ppdb::create([
            'id' => $request->id,
            'nama' => $request->nama,
            'jns_kelamin' => $request->jns_kelamin,
            'temp_lahir' => $request->temp_lahir,
            'tgl_lahir' => $request->tgl_lahir,
            'alamat' => $request->alamat,
            'asal_sekolah' => $request->asal_sekolah,
            'kelas' => $request->kelas,
            'jurusan' => $request->jurusan,
            
         ]);
        return redirect('ppdb');
    }

